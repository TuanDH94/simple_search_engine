def remove_accent(input_str):
    s = ''
    for c in input_str:
        if c in s1:
            s += s0[s1.index(c)]
        else:
            s += c
    return s


def preprocess_text(input_str):
    input_str = input_str.lower()
    special_character = string.digits + string.punctuation
    for c in special_character:
        input_str = input_str.replace(c, '')
    input_str = input_str.replace('\n', '')
    return input_str


